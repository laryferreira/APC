def fahrenheit(t):
    fahrenheit = t * 1.8 + 32
    fahrenheit_print = print(f'Fahrenheit: {fahrenheit}')
    return fahrenheit_print
def kelvin(t):
    kelvin = t + 273.15
    kelvin_print = print(f'Kelvin: {kelvin}')
    return kelvin_print
